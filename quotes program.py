#my favorite quote
quote=("Only two things are infinite, the universe and human stupidity, and I'm not sure about the former.")

print("Original quote")
print(quote)#makes the veriable "quote"

print("\nIn uppercase:")
print(quote.upper())#makes all characters uppercase

print("\nIn lowercase:")
print(quote.lower())#makes all characters lowercase

print("\nIn Title:")
print(quote.title())#first letter of words = capital

print("\nIn swapcase:")
print(quote.swapcase())#swiches the case e.g. T=t and h=H

print("\nIn capitalize:")
print(quote.capitalize())#capitalise first letter of each sentence

print("\nIn replace:")
print(quote.replace("genius", "smartness"))#swaps words


